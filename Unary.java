//Program using Unary Operator
public class Unary
{
  public static void main(String args[])
  {
     int num1=10;
      //pre increment   
     int num2=++num1;//num1=11 , num2=11 

      //post increment
     int num3=num1++;// num3=11, num1=12
     System.out.println("num1="+num1+" num2="+num2+" num3="+num3);
     // 
  }
}